const fs = require('fs');
const path = require('path');
const cnchar = require('cnchar');
const trad = require('cnchar-trad');
cnchar.use(trad);

const inputPath = process.argv[2];
const outputPath = process.argv[3] || path.resolve(__dirname, '../js/data/kangxi-strokes.js');

if (!inputPath) {
    console.error('Usage: node scripts/build-kangxi-strokes.js <kangxi-strokecount.csv> [output]');
    process.exit(1);
}

const lines = fs.readFileSync(path.resolve(inputPath), 'utf8').trim().split(/\r?\n/);
const headerIndex = lines.findIndex((line) => line.trim() === 'CodePoint,Value,Character,Strokes');
if (headerIndex < 0) {
    throw new Error('Kangxi CSV header not found');
}
lines.splice(0, headerIndex + 1);

const strokes = {};
for (const line of lines) {
    const match = line.match(/^([^,]+),([^,]+),(.+),(\d+)$/u);
    if (!match) throw new Error(`Invalid CSV row: ${line}`);
    const character = match[3];
    const count = Number(match[4]);
    strokes[character] = count;
}

const traditionalVariants = {};
Object.keys(strokes).forEach((character) => {
    const traditional = cnchar.convert.simpleToTrad(character);
    if (traditional !== character && Array.from(traditional).length === 1 && Number.isInteger(strokes[traditional])) {
        traditionalVariants[character] = traditional;
    }
});

const generated = `// Generated by scripts/build-kangxi-strokes.js. Do not edit manually.\n` +
`// Source: https://github.com/breezyreeds/kangxi-strokecount (MIT)\n` +
`window.KangxiStrokeData = Object.freeze({\n` +
`    meta: Object.freeze({\n` +
`        source: 'breezyreeds/kangxi-strokecount',\n` +
`        sourceUrl: 'https://github.com/breezyreeds/kangxi-strokecount',\n` +
`        license: 'MIT',\n` +
`        recordCount: ${Object.keys(strokes).length},\n` +
`        variantSource: 'cnchar-trad 3.2.6 (MIT)',\n` +
`        note: '\u5eb7\u7199\u5b57\u5178\u7b14\u753b\u6570\u636e\uff1b\u7b80\u4f53\u8f93\u5165\u4f18\u5148\u8f6c\u4e3a\u7e41\u4f53\u53d6\u7b14\u753b\uff0c\u591a\u4e49\u7b80\u4f53\u5b57\u4ecd\u9700\u4eba\u5de5\u590d\u6838\u3002\u4e94\u884c\u5206\u7c7b\u53e6\u89c1\u59d3\u540d\u5b66\u6574\u7406\u6570\u636e\u3002'\n` +
`    }),\n` +
`    traditionalVariants: Object.freeze(${JSON.stringify(traditionalVariants)}),\n` +
`    strokes: Object.freeze(${JSON.stringify(strokes)})\n` +
`});\n`;

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, generated, 'utf8');
console.log(`Generated ${outputPath} with ${Object.keys(strokes).length} records and ${Object.keys(traditionalVariants).length} simplified variants.`);
